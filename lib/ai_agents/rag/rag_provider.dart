  /// 
  /// [query] 用户查询
  /// [context] 提供额外的上下文信息
  /// 
  /// 返回基于检索增强生成的回答
  Future<String> getAnswer(String query, {String? context}) async {
    try {
      _logger.i('执行RAG查询: $query, 上下文: $context');
      
      // 模拟查询延迟
      await Future.delayed(const Duration(milliseconds: 800));
      
      // 根据上下文主题生成相应的回答
      final answer = _getTopicAnswer(query, context);
      
      return answer;
    } catch (e) {
      _logger.e('RAG查询失败: $e');
      return '抱歉，我无法回答这个问题。';
    }
  }
  
