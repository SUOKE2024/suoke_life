  Future<List<String>> _checkContentSafety(String content) async {
    try {
      final analysis = await _aiService.analyze(
        message: content,
        type: 'content_safety',
      );
      
      final issues = <String>[];
      if (analysis['has_sensitive_content'] == true) {
        issues.add('内容包含敏感信息');
      }
      if (analysis['has_harmful_content'] == true) {
        issues.add('内容可能存在危害');
      }
      
      return issues;
    } catch (e) {
      debugPrint('内容安全检查失败: $e');
      return [];
    }
  }

