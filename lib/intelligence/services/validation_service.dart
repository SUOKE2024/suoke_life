      id: 'validation_${DateTime.now().millisecondsSinceEpoch}',
      userId: userId ?? 'system',
      assistantName: 'system',
      type: AIEventType.validation,
      data: {
        'rule_type': ruleType,
        'is_valid': isValid,
        'error': error?.toString(),
      },
    ));
  }
