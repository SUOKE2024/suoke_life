class ValidationService extends GetxService {
  final AIService _aiService;
  final SecurityManagerService _securityManager;
  final EventTrackingService _eventTracking;
  final SubscriptionService _subscriptionService;
  
  // 验证规则配置
  static const Map<String, ValidationRule> _validationRules = {
    'user_input': ValidationRule(
      required: true,
      minLength: 1,
      maxLength: 1000,
      patterns: [
        r'^[^\s].*[^\s]$',  // 不以空白开始和结束
      ],
      sanitize: true,
    ),
    'sensitive_data': ValidationRule(
      required: true,
      encrypted: true,
      patterns: [
        r'^(?=.*[A-Za-z])(?=.*\d)[A-Za-z\d]{8,}$',  // 至少8位字母数字组合
      ],
      sanitize: true,
    ),
    'system_command': ValidationRule(
      required: true,
      patterns: [
        r'^[a-zA-Z_][a-zA-Z0-9_]*$',  // 合法的命令格式
      ],
      whitelist: ['start', 'stop', 'restart', 'status'],
      sanitize: false,
    ),
  };
  
  ValidationService({
    required AIService aiService,
    required SecurityManagerService securityManager,
    required EventTrackingService eventTracking,
    required SubscriptionService subscriptionService,
  })  : _aiService = aiService,
        _securityManager = securityManager,
        _eventTracking = eventTracking,
        _subscriptionService = subscriptionService;

  Future<ValidationResult> validate(
    String data,
    String ruleType, {
    Map<String, dynamic>? options,
    String? userId,
  }) async {
    try {
      // 获取验证规则
      final rule = _getValidationRule(ruleType);
      
      // 执行验证
      final result = await _validateData(data, rule, options);
      
      // 记录验证事件
      await _trackValidationEvent(
        ruleType,
        result.isValid,
        userId: userId,
      );

      return result;
    } catch (e) {
      await _trackValidationEvent(
        ruleType,
        false,
        error: e,
        userId: userId,
      );
      rethrow;
    }
  }

  Future<String> sanitize(
    String data,
    String ruleType, {
    Map<String, dynamic>? options,
  }) async {
    try {
      // 获取验证规则
      final rule = _getValidationRule(ruleType);
      if (!rule.sanitize) return data;
      
      // 执行清理
      String sanitized = data;
      
      // 基本清理
      sanitized = sanitized.trim();
      sanitized = _removeControlCharacters(sanitized);
      
      // 自定义清理规则
      if (options?['custom_sanitize'] != null) {
        sanitized = await _customSanitize(
          sanitized,
          options!['custom_sanitize'],
        );
      }
      
      return sanitized;
    } catch (e) {
      throw AIException(
        '数据清理失败',
        code: 'SANITIZE_ERROR',
        details: e,
      );
    }
  }

  Future<bool> isValid(
    String data,
    String ruleType, {
    Map<String, dynamic>? options,
  }) async {
    final result = await validate(data, ruleType, options: options);
    return result.isValid;
  }

  ValidationRule _getValidationRule(String ruleType) {
    final rule = _validationRules[ruleType];
    if (rule == null) {
      throw AIException(
        '未知的验证规则类型',
        code: 'UNKNOWN_RULE_TYPE',
      );
    }
    return rule;
  }

  Future<ValidationResult> _validateData(
    String data,
    ValidationRule rule,
    Map<String, dynamic>? options,
  ) async {
    final errors = <String>[];
    
    // 检查必填
    if (rule.required && data.isEmpty) {
      errors.add('数据不能为空');
    }

    // 检查长度
    if (rule.minLength != null && data.length < rule.minLength!) {
      errors.add('数据长度不能小于 ${rule.minLength}');
    }
    if (rule.maxLength != null && data.length > rule.maxLength!) {
      errors.add('数据长度不能大于 ${rule.maxLength}');
    }

    // 检查模式
    for (final pattern in rule.patterns) {
      if (!RegExp(pattern).hasMatch(data)) {
        errors.add('数据格式不符合要求');
        break;
      }
    }

    // 检查白名单
    if (rule.whitelist != null && !rule.whitelist!.contains(data)) {
      errors.add('数据不在允许的范围内');
    }

    // 检查黑名单
    if (rule.blacklist != null && rule.blacklist!.contains(data)) {
      errors.add('数据在禁止的范围内');
    }

    // 自定义验证
    if (options?['custom_validation'] != null) {
      final customErrors = await _customValidation(
        data,
        options!['custom_validation'],
      );
      errors.addAll(customErrors);
    }

    // 内容安全检查
    if (rule.contentSafety ?? false) {
      final safetyErrors = await _checkContentSafety(data);
      errors.addAll(safetyErrors);
    }

    return ValidationResult(
      isValid: errors.isEmpty,
      errors: errors,
      sanitizedData: rule.sanitize ? await sanitize(data, rule.type) : data,
    );
  }

  String _removeControlCharacters(String input) {
    return input.replaceAll(RegExp(r'[\x00-\x1F\x7F]'), '');
  }

  Future<String> _customSanitize(
    String data,
    Map<String, dynamic> rules,
  ) async {
    // TODO: 实现自定义清理逻辑
    return data;
