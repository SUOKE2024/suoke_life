  static String translate(String key, String locale) {
    final languageCode = locale.split('_')[0];
    final countryCode = locale.split('_')[1];
    final fullLocale = '${languageCode}_${countryCode}';

    return translations[fullLocale]?[key] ??
        translations['en_US']?[key] ??
        key;
  }

  static bool hasTranslation(String key, String locale) {
    final languageCode = locale.split('_')[0];
    final countryCode = locale.split('_')[1];
    final fullLocale = '${languageCode}_${countryCode}';

    return translations[fullLocale]?.containsKey(key) ?? false;
  }

  static List<String> getAvailableLocales() {
    return translations.keys.toList();
  }

  static Map<String, String> getAllTranslations(String locale) {
    final languageCode = locale.split('_')[0];
    final countryCode = locale.split('_')[1];
    final fullLocale = '${languageCode}_${countryCode}';

    return translations[fullLocale] ?? translations['en_US'] ?? {};
  }
